    private void jButtonOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonOkActionPerformed

        Pessoa pessoa = new Pessoa();

        if (jTextFieldNome.getText().isEmpty() || jTextFieldsobrenome.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "O usuario deve fornecer nome e sobrenome", "Erro de utilização", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                String msg = "Bem vindo" + jTextFieldNome.getText() + " " + jTextFieldsobrenome.getText();
                JOptionPane.showMessageDialog(rootPane, msg, "Erro de utilização", JOptionPane.INFORMATION_MESSAGE);
                pessoa.setNome(jTextFieldsobrenome.getText());
                pessoa.setSobrenome(jTextFieldsobrenome.getText());
                int idade = Integer.parseInt(jTextFieldIdade.getText());
                pessoa.setIdade(idade);
                v.add(pessoa);

                br.write(pessoa.toStringFile());
                br.newLine();
                br.flush();
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(rootPane, ex.getMessage());
            }

        }
        System.out.println(v);
    }//GEN-LAST:event_jButtonOkActionPerformed

