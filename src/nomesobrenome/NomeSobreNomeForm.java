    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Pessoa pessoa = new Pessoa();
            String textFile = new String();
            File arquivo = new File("arquivo.txt");
            FileReader learquivo = new FileReader(arquivo);
            BufferedReader br = new BufferedReader(learquivo);
            
            String line = br.readLine();
            while (line != null) {
                StringTokenizer st = new StringTokenizer(line, "#");
                String nome = st.nextToken();
                String sobrenome = st.nextToken();   
                String idade = st.nextToken();
                pessoa.setNome(nome);
                pessoa.setSobrenome(sobrenome);
                int idadeInt = Integer.parseInt(idade);
                pessoa.setIdade(idadeInt);

                JOptionPane.showMessageDialog(this, pessoa.toString());

                textFile = textFile + "\n" + line;
                line = br.readLine();
                
            }
            
            JOptionPane.showMessageDialog(this, textFile);

        } catch (IOException ex) {
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

